#include <bits/stdc++.h>

using namespace std;

int main() {
    int TC, cas = 1; cin >> TC;
    while(TC--) {
        string s;
        while(cin >> s) {
            if (s.empty()) break;

        }
        if (cas++ > 1) printf("\n");
    }

    return 0;
}